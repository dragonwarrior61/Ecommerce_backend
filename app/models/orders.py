from sqlalchemy import Column, String, Integer, Float, DateTime, PrimaryKeyConstraint, ARRAY, BigInteger
from app.database import Base

class Order(Base):
    __tablename__ = "orders"
    id = Column(BigInteger, index=True)
    vendor_name = Column(String, nullable=True)
    type = Column(Integer, nullable=True)
    date = Column(DateTime, nullable=True)
    payment_mode = Column(String, nullable=True)
    detailed_payment_method = Column(String, nullable=True)
    delivery_mode = Column(String, nullable=True)
    status = Column(Integer, nullable=True)
    payment_status = Column(Integer, nullable=True)
    customer_id = Column(BigInteger, nullable=True)
    product_id = Column(ARRAY(String), nullable=True)
    quantity = Column(ARRAY(Integer), nullable=True)
    initial_quantity = Column(ARRAY(Integer), nullable=True)
    sale_price = Column(ARRAY(Float), nullable=True)
    shipping_tax = Column(Float, nullable=True)
    shipping_tax_voucher_split = Column(String, nullable=True)
    vouchers = Column(String, nullable=True)
    proforms = Column(String, nullable=True)
    attachments = Column(String, nullable=True)
    shipping_address = Column(String, nullable=True)
    cashed_co = Column(Float, nullable=True)
    cashed_cod = Column(Float, nullable=True)
    refunded_amount = Column(Float, nullable=True)
    is_complete = Column(Integer, nullable=True)
    cancellation_request = Column(String, nullable=True)
    cancellation_reason = Column(String, nullable=True)
    refund_status = Column(String, nullable=True)
    maximum_date_for_shipment = Column(DateTime, nullable=True)
    late_shipment = Column(Integer, nullable=True)
    flags = Column(String, nullable=True)
    emag_club = Column(Integer, nullable=True)
    finalization_date = Column(DateTime, nullable=True)
    details = Column(String, nullable=True)
    payment_mode_id = Column(Integer, nullable=True)    
    order_market_place = Column(String, nullable=True)
    mkt_id = Column(BigInteger, nullable=True)
    name = Column(String, nullable=True)
    company = Column(String, nullable=True)
    gender = Column(String, nullable=True)
    phone_1 = Column(String, nullable=True)
    billing_name = Column(String, nullable=True)
    billing_phone = Column(String, nullable=True)
    billing_country = Column(String, nullable=True)
    billing_suburb = Column(String, nullable=True)
    billing_city = Column(String, nullable=True)
    billing_locality_id = Column(String, nullable=True)
    billing_street = Column(String, nullable=True)
    shipping_country = Column(String, nullable=True)
    shipping_suburb = Column(String, nullable=True)
    shipping_city = Column(String, nullable=True)
    shipping_locality_id = Column(String, nullable=True)
    shipping_contact = Column(String, nullable=True)
    shipping_phone = Column(String, nullable=True)
    shipping_street = Column(String, nullable=True)
    created = Column(DateTime, nullable=True)
    modified = Column(DateTime, nullable=True)
    legal_entity = Column(Integer, nullable=True)
    is_vat_payer = Column(Integer, nullable=True)
    code = Column(String, nullable=True)
    bank = Column(String, nullable=True)
    iban = Column(String, nullable=True)
    email = Column(String, nullable=True)
    product_voucher_split = Column(ARRAY(String), nullable=True)
    registration_number = Column(String, nullable=True)
    update_time = Column(DateTime, nullable=True)
    packing_status = Column(Integer, nullable=True)
    user_id = Column(Integer, index=True)
    
    __table_args__ = (
        PrimaryKeyConstraint('id', 'user_id', name = 'pk_id_user_id'),
    )
